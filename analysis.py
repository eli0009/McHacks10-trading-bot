def analyze(article: str, sentiment=True):
    score = 0
    article = article.lower()
    for word in wordsp if sentiment else wordsn:
        if word in article:
            score += 1

    word_count = len(article.split())
    sentiment_score = int(score / word_count * 10000) / 100
    print(f'{"Positive" if sentiment else "Negative"} score: {sentiment_score}%')
    print(f"Total words: {word_count}")
    print(f"Count: {score}")

    return {
        "occurrence": score,
        "word_count": word_count,
        "sentiment_score": sentiment_score,
    }


analyze(article, True)
analyze(article, False)
